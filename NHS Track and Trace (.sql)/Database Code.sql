CREATE DATABASE ASSIGNMENT2;

DROP TABLE PATIENT;
DROP TABLE TEST_CENTRE;
DROP TABLE TEST;
DROP TABLE TEST_RECORD;
DROP TABLE LOCATION;

CREATE TABLE PATIENT (
    NHS_Number          BIGINT UNIQUE NOT NULL,
    Pat_FName           VARCHAR(25) NOT NULL,
    Pat_LName           VARCHAR(25) NOT NULL,
    Pat_DOB             DATE NOT NULL,
    Pat_Tel             VARCHAR(15) UNIQUE NOT NULL,
    Pat_Address_1       VARCHAR(50) NOT NULL,
    Pat_Town_City       VARCHAR(25) NOT NULL,
    Pat_County          VARCHAR(35) NOT NULL,
    Pat_PostCode        VARCHAR(8) NOT NULL,
    Pat_Country         VARCHAR(25) NOT NULL,
    PRIMARY KEY (NHS_Number)
);

CREATE TABLE TEST_CENTRE(
    Centre_ID           INT UNIQUE NOT NULL,
    Centre_Name         VARCHAR(50) NOT NULL,
    Centre_Address      VARCHAR(25) NOT NULL,
    Centre_Town         VARCHAR(25) NOT NULL,
    Centre_PostCode     VARCHAR(25) NOT NULL,
    Centre_Country      VARCHAR(25) NOT NULL,
    PRIMARY KEY(Centre_ID)
);

CREATE TABLE TEST(
    Test_ID             BIGINT UNIQUE NOT NULL,
    Patient_Number      BIGINT NOT NULL,
    Test_Loc            INT,/*Tests completed at home using a LFT will not have an associated test_loc*/
    Test_Date           DATE NOT NULL,
    PRIMARY KEY (Test_ID),
    FOREIGN KEY (Patient_Number) REFERENCES PATIENT(NHS_Number),
    FOREIGN KEY (Test_Loc) REFERENCES TEST_CENTRE(Centre_ID)
);

CREATE TABLE TEST_RECORD(
    Test_ID             BIGINT UNIQUE NOT NULL,
    Patient_Number      BIGINT NOT NULL,
    Test_Result         BOOLEAN NOT NULL,
    Virus_Variant       VARCHAR(5) NOT NULL,
    FOREIGN KEY (Test_ID) REFERENCES TEST(Test_ID),
    FOREIGN KEY (Patient_Number) REFERENCES PATIENT(NHS_Number)
);

CREATE TABLE LOCATION(
    Location_ID         BIGINT UNIQUE NOT NULL,
    Location_Name       VARCHAR(25),
    Location_Address1   VARCHAR(25),
    Location_PostCode   VARCHAR(8),
    PRIMARY KEY (Location_ID)
);

CREATE TABLE VISITS(
    Visit_ID            BIGINT UNIQUE NOT NULL,
    Location_ID         BIGINT NOT NULL,
    Lat_Long            POINT NOT NULL,
    Visitor_ID          BIGINT NOT NULL,
    Visit_Timestamp     TIMESTAMP NOT NULL,
    PRIMARY KEY (Visit_ID),
    FOREIGN KEY (Location_ID) REFERENCES LOCATION(Location_ID),
    FOREIGN KEY (Visitor_ID) REFERENCES PATIENT(NHS_Number)
);

CREATE TABLE CONTACT(
    Contact_ID          INT UNIQUE NOT NULL,
    Patient_Number      BIGINT NOT NULL,
    Contact_Loc         BIGINT,/*Contacts not necessarily made in locations listed in locations table, so NOT NULL no longer relevant*/
    Contact_Timestamp   TIMESTAMP NOT NULL,
    Person_ID           BIGINT NOT NULL,/* Would refer to NHS_Numbers from outside of the current database, but would be a FK against PATIENT(NHS_Number)*/
    PRIMARY KEY (Contact_ID),
    FOREIGN KEY (Patient_Number) REFERENCES PATIENT(NHS_Number),
    FOREIGN KEY (Contact_Loc) REFERENCES VISITS(Visit_ID)
);

